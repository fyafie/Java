public class PosisiKerja {
    //deklarasi variabel
    private int idPosisiKerja;
    private String namaPosisiKerja;

    public PosisiKerja() {
    }

    public int getIdPosisiKerja() {
        return this.idPosisiKerja;
    }

    public void setIdPosisiKerja(int idPosisiKerja) {
        this.idPosisiKerja = idPosisiKerja;
    }

    public String getNamaPosisiKerja() {
        return this.namaPosisiKerja;
    }

    public void setNamaPosisiKerja(String namaPosisiKerja) {
        this.namaPosisiKerja = namaPosisiKerja;
    }
}